#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: First number
 * @b: Second number
 * Return: Result of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 * @a: First number
 * @b: Second number
 * Return: Result of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: First number
 * @b: Second number
 * Return: Result of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: First number
 * @b: Second number
 * Return: Result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two numbers
 * @a: First number
 * @b: Second number
 * Return: Result of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
